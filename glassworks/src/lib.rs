pub fn ecb_calc(row: &[u8]) -> u8 {
    row.iter().map(|b| *b as u16).reduce(|acc, b| {
        let acc_carry = acc >> 8;
        let acc_value = acc & 0xff;
        acc_value + b + (1 - acc_carry)
    }).unwrap() as u8
}

